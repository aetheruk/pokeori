import { existsSync, mkdirSync, readdirSync, writeFileSync, statSync } from 'fs'
import { basename, dirname, extname, join, relative } from 'path'
import { fileURLToPath } from 'url'
import { items } from '../src/data/items/index.ts'
import { evolutionItems } from '../src/data/items/entries/evolution.ts'
import { megaStones } from '../src/data/items/entries/mega-stones.ts'
import { tmItems } from '../src/data/items/entries/tms.ts'

const repoRoot = join(dirname(fileURLToPath(import.meta.url)), '..')
const itemSpritesDir = join(repoRoot, 'public', 'sprites', 'items')
const manifestPath = join(repoRoot, 'src', 'data', 'item-sprite-manifest.json')
const missingDocPath = join(repoRoot, 'docs', 'game-data', 'missing-item-sprites.md')

const imageExtensions = ['.avif', '.webp', '.png', '.jpg', '.jpeg', '.gif']
const evolutionItemIds = new Set(evolutionItems.map((item) => item.id))
const megaStoneItemIds = new Set(megaStones.map((item) => item.id))
const tmItemIds = new Set(tmItems.map((item) => item.id))
const evolutionSpriteIds = new Set(evolutionItems.map((item) => normalizeSpriteId(item.spriteId || item.id)))
const megaStoneSpriteIds = new Set(megaStones.map((item) => normalizeSpriteId(item.spriteId || item.id)))
const tmSpriteIds = new Set(tmItems.map((item) => normalizeSpriteId(item.spriteId || item.id)))

function normalizeSpriteId(id) {
  return id.replace(/^\/+/, '')
}

function sortedEntries(record) {
  return Object.fromEntries(Object.entries(record).sort(([a], [b]) => a.localeCompare(b)))
}

function walkFiles(dir) {
  if (!existsSync(dir)) return []

  return readdirSync(dir).flatMap((entry) => {
    const path = join(dir, entry)
    const stats = statSync(path)
    if (stats.isDirectory()) {
      return walkFiles(path)
    }

    return stats.isFile() ? [path] : []
  })
}

function buildLocalIndex() {
  const byExactId = new Map()
  const byIdWithoutExtension = new Map()
  const byBasenameWithoutExtension = new Map()

  for (const file of walkFiles(itemSpritesDir)) {
    const rel = relative(itemSpritesDir, file)
    const normalizedRel = rel.split('\\').join('/')
    if (!imageExtensions.includes(extname(normalizedRel).toLowerCase())) continue

    const publicPath = `/sprites/items/${normalizedRel}`
    byExactId.set(normalizedRel, publicPath)

    const extension = extname(normalizedRel)
    if (extension) {
      byIdWithoutExtension.set(normalizedRel.slice(0, -extension.length), publicPath)
      byBasenameWithoutExtension.set(basename(normalizedRel, extension), publicPath)
    }
  }

  return { byExactId, byIdWithoutExtension, byBasenameWithoutExtension }
}

function resolveLocalSprite(index, spriteId) {
  return (
    index.byExactId.get(spriteId) ||
    index.byIdWithoutExtension.get(spriteId) ||
    index.byBasenameWithoutExtension.get(spriteId) ||
    null
  )
}

function preferredRelFor(item, spriteId) {
  const filename = basename(extname(spriteId) ? spriteId : `${spriteId}.avif`)

  if (item.id.startsWith('binder-') || item.id.startsWith('pack-')) {
    return `tcg/${filename}`
  }

  if (megaStoneItemIds.has(item.id) || megaStoneSpriteIds.has(spriteId)) {
    return `mega/${filename}`
  }

  if (
    tmItemIds.has(item.id) ||
    tmSpriteIds.has(spriteId) ||
    item.id.startsWith('tm-') ||
    spriteId.startsWith('tm-')
  ) {
    return `tm/${filename}`
  }

  if (spriteId.endsWith('-gem')) {
    return `materials/${filename}`
  }

  if (evolutionItemIds.has(item.id) || evolutionSpriteIds.has(spriteId)) {
    return `evolution/${filename}`
  }

  if (extname(spriteId)) return spriteId
  return `${spriteId}.avif`
}

function preferredPathFor(item, spriteId) {
  return `/sprites/items/${preferredRelFor(item, spriteId)}`
}

function writeMissingDoc(missing) {
  const rows = missing
    .map(
      (entry) =>
        `| \`${entry.itemId}\` | ${entry.name} | \`${entry.category}\` | \`${entry.spriteId}\` | \`${entry.expectedPath}\` |`,
    )
    .join('\n')

  const content = `<!-- item-sprite-sync -->
# Missing Item Sprites

Generated by \`bun run sync:item-sprites\`.

These referenced item sprites do not exist in \`public/sprites/items\`.

| Item ID | Name | Category | Sprite ID | Expected Path |
| --- | --- | --- | --- | --- |
${rows || '| None | None | None | None | None |'}
`

  writeFileSync(missingDocPath, content)
}

mkdirSync(itemSpritesDir, { recursive: true })

const localIndex = buildLocalIndex()
const spritePaths = {}
const itemPaths = {}
const missingByItem = new Map()

for (const [spriteId, spritePath] of localIndex.byIdWithoutExtension) {
  spritePaths[spriteId] = spritePath

  const baseSpriteId = basename(spriteId)
  if (!spritePaths[baseSpriteId]) {
    spritePaths[baseSpriteId] = spritePath
  }
}

for (const item of items) {
  const spriteId = normalizeSpriteId(item.spriteId || item.id)
  const preferredPath = preferredPathFor(item, spriteId)
  const spritePath =
    resolveLocalSprite(localIndex, preferredRelFor(item, spriteId)) ||
    resolveLocalSprite(localIndex, spriteId)

  if (spritePath) {
    spritePaths[spriteId] = spritePath
    itemPaths[item.id] = spritePath
    continue
  }

  missingByItem.set(item.id, {
    itemId: item.id,
    name: item.name,
    category: item.category,
    spriteId,
    expectedPath: preferredPath,
  })
}

const missing = [...missingByItem.values()].sort((a, b) => a.itemId.localeCompare(b.itemId))
const manifest = {
  basePath: '/sprites/items',
  items: sortedEntries(itemPaths),
  sprites: sortedEntries(spritePaths),
  missing,
}

writeFileSync(`${manifestPath}\n`.trim(), `${JSON.stringify(manifest, null, 2)}\n`)
writeMissingDoc(missing)

console.log(`Item sprite sync complete.`)
console.log('- copied sprites: 0')
console.log(`- resolved item sprites: ${Object.keys(itemPaths).length}`)
console.log(`- missing item sprites: ${missing.length}`)
