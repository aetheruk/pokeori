import type { Item } from '../types'

function tieredMaterial(
  family: string,
  name: string,
  descriptions: [string, string, string],
  spriteId: string,
): Item[] {
  return [
    {
      id: `${family}-t1`,
      name,
      description: descriptions[0],
      category: 'misc',
      spriteId,
      hueRotate: 0,
      sellValue: 10,
    },
    {
      id: `${family}-t2`,
      name: `${name}+`,
      description: descriptions[1],
      category: 'misc',
      spriteId,
      hueRotate: 40,
      sellValue: 10,
    },
    {
      id: `${family}-t3`,
      name: `${name} EX`,
      description: descriptions[2],
      category: 'misc',
      spriteId,
      hueRotate: 80,
      sellValue: 10,
    },
  ]
}

