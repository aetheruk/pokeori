function unbeatenBattle(id: string) {
  return {
    type: 'battle_result' as const,
    targetId: id,
    battleStatus: 'win' as const,
    count: 1,
    inverse: true,
  }
}

function echoMapWin(targetId: string) {
  return {
    type: 'research_encounter_result' as const,
    targetId,
    battleStatus: 'win' as const,
    count: 1,
  }
}

function catchWins(targetId: string, count: number) {
  return {
    type: 'location_encounter_result' as const,
    targetId,
    battleStatus: 'win' as const,
    count,
  }
}

function wildBattleWins(targetId: string, count: number) {
  return {
    type: 'battle_result' as const,
    targetId,
    battleStatus: 'win' as const,
    count,
  }
}

function localProgress(locationId: string, battleId: string, count: number) {
  return [catchWins(locationId, count), wildBattleWins(battleId, count)]
}

function totemReward(targetId: string) {
  return {
    type: 'item' as const,
    targetId,
    quantity: 1,
    dropChance: 2,
    secret: true,
  }
}

